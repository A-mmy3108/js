
let score = 33
console.log(typeof score); // typeof is used to known the datatype) 


//CONVERSION ( CONVERTING A VALUE TO NUMBER VALUE )
let valueInNumber = Number(score)
console.log(typeof valueInNumber)
console.log(valueInNumber)

