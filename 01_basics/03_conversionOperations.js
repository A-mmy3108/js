//CONVERSION ( CONVERTING A VALUE TO BOOLEAN TYPE )
let isLoggedIn = 1

let booleanIsLoggedIn = Boolean(isLoggedIn)
console.log(booleanIsLoggedIn);

// 1 = true 
// 0 = false
